# operasi list

data = [1,2,4,2,5,2,4,3,9,2,2,7,2,6]
# data.count untuk menghitung jumlah data yang sama
print(data.count(2))

# data.index untuk melihat index data tsb
print(data.index(2))

# data.sort() untuk mengurutkan data
data.sort()
print(data)

# data.reverse() untuk membalik urutan
data.reverse()
print(data)
