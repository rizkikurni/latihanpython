# simbol2 komparasi: <, >, <=, >=, ==, !=, is(podo == tapi luwih ringkas), is not(podo =! tapi luwih ringkas)

a = 5
b = 8

print("a > b =", a > b)
print("a < b =", a < b)
print("a >= b =", a >= b)
print("a <= b =", a <= b)
print("a == b =", a == b)
print("a != b =", a != b)
print("a is b =", a is b)
print("a is not b =", a is not b)